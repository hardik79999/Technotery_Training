# Slicing

x = "hello world"
print(x[3:7])

#-------------------------------------------------------------------
# Slice From the Start

c = "hello world"
print(c[:8])

#-------------------------------------------------------------------
# Slice To the End

c = "hello world"
print(c[3:])
#-------------------------------------------------------------------
# Negative Indexing

#    0  <-- positive
c = "hello world"
# negative--> -1
   
print(c[-8:-1])
#-------------------------------------------------------------------