# Use the copy() method

thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist) # output : ['apple', 'banana', 'cherry']


# Use the list() method--------------------------------------------
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)
print(mylist) # output : ['apple', 'banana', 'cherry']


# Use the slice Operator---------------------------------------
thislist = ["apple", "banana", "cherry"]
mylist = thislist[:]
print(mylist) # output : ['apple', 'banana', 'cherry']