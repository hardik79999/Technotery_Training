thislist = ["apple", "banana", "cherry"]
print(thislist[1]) # output : banana


# Negative Indexing

thislist = ["apple", "banana", "cherry"]
print(thislist[-1]) # output : cherry



# Range of Indexes

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:5]) # output : ['cherry', 'orange', 'kiwi']